'use client';

import React, { useState } from 'react';
import dayjs from 'dayjs';
import { ActionIcon, Button, Group, Modal, Popover, Stack } from '@mantine/core';
import { TbEdit } from 'react-icons/tb';
import { SlOptionsVertical } from 'react-icons/sl';
import { MdOutlineCalendarMonth } from 'react-icons/md';
import { GrTrash } from 'react-icons/gr';
import classes from './pages-table.module.scss';
import { IPageResponse } from '@/interfaces/page-interface';
import { EntitiesTableColumnProps } from '../entities-table/_props';
import { DatePicker } from '@mantine/dates';
import EntitiesTable from '../entities-table/entities-table';
import { PageTypeDisplayMap, StatusDisplayMap } from '@/constants';
import { useRouter } from 'next/navigation';
import { deletePageAction } from '@/actions/page-action';
import { notifications } from '@mantine/notifications';

interface PagesTableProps {
  pagesData: IPageResponse[];
}

export default function PagesTable({
  pagesData,
}: PagesTableProps) {
  const router = useRouter();
  const [datePickerOpened, setDatePickerOpened] = useState(false);
  const [selectedDate, setSelectedDate] = useState<Date | string | null>(null);
  const [deleteModalOpened, setDeleteModalOpened] = useState(false);
  const [isDeleting, setIsDeleting] = useState(false);
  const [selectedPageId, setSelectedPageId] = useState<string | null>(null);

  const handleDeletePage = async () => {
    if (!selectedPageId) return;

    setIsDeleting(true);
    try {
      const result = await deletePageAction(selectedPageId);
      if (result.success) {
        notifications.show({
          message: 'Page has been successfully deleted',
          color: 'green',
          position: 'top-center',
        });
        router.refresh();
      } else {
        notifications.show({
          title: 'Delete failed',
          message: result.error || 'Failed to delete page',
          color: 'red',
        });
      }
    } catch (error) {
      notifications.show({
        title: 'Delete failed',
        message: error instanceof Error ? error.message : 'Failed to delete page',
        color: 'red',
      });
    } finally {
      setIsDeleting(false);
      setDeleteModalOpened(false);
      setSelectedPageId(null);
    }
  };

  const columns: EntitiesTableColumnProps<IPageResponse>[] = [
    {
      key: 'date',
      width: '5%',
      headerAlign: 'left',
      header: (
        <Popover opened={datePickerOpened} onChange={setDatePickerOpened} position='bottom-start'>
          <Popover.Target>
            <ActionIcon
              variant="transparent"
              onClick={() => setDatePickerOpened((o) => !o)}
            >
              <MdOutlineCalendarMonth size={24} color="#01426e" />
            </ActionIcon>
          </Popover.Target>
          <Popover.Dropdown>
            <DatePicker
              value={selectedDate}
              onChange={(value) => setSelectedDate(value)}
            />
          </Popover.Dropdown>
        </Popover>
      ),
      render: ({ entity }) => <>{dayjs(entity.createdAt).format('DD/MM')}</>,
    },
    {
      key: 'title',
      width: '30%',
      header: 'Title',
      render: ({ entity }) => (
        <>
          {entity.title || '(No title)'}
        </>
      ),
    },
    {
      key: 'type',
      width: '15%',
      headerAlign: 'left',
      cellAlign: 'left',
      header: 'Type',
      render: ({ entity }) => <>{PageTypeDisplayMap[entity.type] || '-'}</>,
    },
    {
      key: 'creator',
      width: '15%',
      headerAlign: 'left',
      cellAlign: 'left',
      header: 'Author',
      render: ({ entity }) => {
        return <>{entity?.createdBy?.name}</>;
      },
    },
    {
      key: 'status',
      width: '10%',
      header: 'Status',
      render: ({ entity }) => (StatusDisplayMap[entity.visibility]),
    },
    {
      key: 'actions',
      width: '10%',
      headerAlign: 'right',
      header: (
        <div className={`${classes.columnHeaderContent} ${classes.actionColumnHeaderContent}`}>
          <ActionIcon variant="transparent">
            <SlOptionsVertical size={24} color="#01426e" />
          </ActionIcon>
        </div>
      ),
      cellAlign: 'right',
      render: ({ entity }) => (
        <Group gap="xs" justify="flex-end">
          <ActionIcon
            variant="transparent"
            onClick={() => {
              setSelectedPageId(entity.id);
              setDeleteModalOpened(true);
            }}
          >
            <GrTrash size={20} color="#01426e" />
          </ActionIcon>
          <ActionIcon
            variant="transparent"
            onClick={() => router.push(`/admin/page/${entity.id}`)}
          >
            <TbEdit size={24} color="#01426e" />
          </ActionIcon>
        </Group>
      ),
    },
  ];


  return (
    <>
      <EntitiesTable<IPageResponse>
        data={pagesData}
        loading={false}
        columns={columns}
        classNames={{
          wrapper: classes.tableWrapper,
          table: {
            table: classes.table,
            thead: classes.thead,
            tbody: classes.tbody,
            tr: classes.tr,
            th: classes.th,
            td: classes.td,
          },
        }}
      />
      <Modal
        opened={deleteModalOpened}
        onClose={() => setDeleteModalOpened(false)}
        title="Confirm Delete"
        centered
      >
        <Stack>
          <Group justify="flex-end" mt="sm">
            <Button
              variant="default"
              onClick={() => setDeleteModalOpened(false)}
              disabled={isDeleting}
            >
              Cancel
            </Button>
            <Button
              color="red"
              onClick={handleDeletePage}
              loading={isDeleting}
            >
              Delete
            </Button>
          </Group>
        </Stack>
      </Modal>
    </>
  );
}

